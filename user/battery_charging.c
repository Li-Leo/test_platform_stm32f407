long map(long x, long in_min, long in_max, long out_min, long out_max) {
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

void OLED_ShowCustomNum(int16_t X, int16_t Y, int n)
{
	if (n < 10)
	{
		OLED_ShowImage(X, Y, 12, 16, custom_num_12X16[n]);
	}
	else if (n >= 10 && n < 100)
	{
		OLED_ShowImage(X-12, Y, 12, 16, custom_num_12X16[n/10]);
		OLED_ShowImage(X, Y, 12, 16, custom_num_12X16[n%10]);
	}
	else if (n < 999)
	{
		OLED_ShowImage(X-24, Y, 12, 16, custom_num_12X16[n/100]);
		OLED_ShowImage(X-12, Y, 12, 16, custom_num_12X16[n/10%10]);
		OLED_ShowImage(X, Y, 12, 16, custom_num_12X16[n%10]);
	}
}

